                <p>
                    Uhitch payment processing costs are non-refundable.
                    Processing costs are the charges incurred by Uhitch when transferring money between a passenger and a driver. The processing costs are calculated as follows:<br>
                    credit card: 2.9% + $0.30<br>
                    bank account: 1% + $0.30 ($5 cap)<br><br>
                    A reservation is not officially canceled until Uhitch sends a cancellation confirmation email.
                    If a cancellation email is not received, contact Uhitch at info@Uhitch.us.
                    Credit card charges usually take up to 5-7 business days to be removed depending on your bank or credit card company.
                </p>
